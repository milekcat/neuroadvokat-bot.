async def message_handler(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    user_id = update.message.from_user.id
    current_state_data = user_states.get(user_id)

    if not current_state_data or not current_state_data.get('state'):
        # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø—Ä–æ—Å—Ç–æ –ø–∏—à–µ—Ç –∏–ª–∏ –ø—Ä–∏—Å—ã–ª–∞–µ—Ç —Ñ–∞–π–ª—ã
        if update.message.document or update.message.photo:
            user_info = update.message.from_user
            caption = f"üìÑ –§–∞–π–ª –æ—Ç {user_info.full_name} ([@{user_info.username}](tg://user?id={user_id}))"
            await context.bot.forward_message(chat_id=CHAT_ID_FOR_ALERTS, from_chat_id=user_id, message_id=update.message.message_id)
            await context.bot.send_message(chat_id=CHAT_ID_FOR_ALERTS, text=caption, parse_mode='Markdown')
            await update.message.reply_text("üìé –§–∞–π–ª –ø–æ–ª—É—á–µ–Ω. –ï—Å–ª–∏ –æ–Ω –æ—Ç–Ω–æ—Å–∏—Ç—Å—è –∫ –∑–∞—è–≤–∫–µ, —Å–ø–µ—Ü–∏–∞–ª–∏—Å—Ç –µ–≥–æ —É–≤–∏–¥–∏—Ç.")
        else:
            await update.message.reply_text("–ß—Ç–æ–±—ã –Ω–∞—á–∞—Ç—å, –≤–æ—Å–ø–æ–ª—å–∑—É–π—Ç–µ—Å—å –∫–æ–º–∞–Ω–¥–æ–π /start")
        return

    state = current_state_data['state']

    if state == 'ask_name':
        user_states[user_id]['name'] = update.message.text
        user_states[user_id]['state'] = 'ask_description'
        await update.message.reply_text(
            f"–ü—Ä–∏—è—Ç–Ω–æ –ø–æ–∑–Ω–∞–∫–æ–º–∏—Ç—å—Å—è, {user_states[user_id]['name']}!\n\n"
            "–¢–µ–ø–µ—Ä—å, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞, **–≤ –æ–¥–Ω–æ–º —Å–æ–æ–±—â–µ–Ω–∏–∏** –æ–ø–∏—à–∏—Ç–µ –≤–∞—à—É —Å–∏—Ç—É–∞—Ü–∏—é. "
            "–ù–µ –≤–æ–ª–Ω—É–π—Ç–µ—Å—å –æ —é—Ä–∏–¥–∏—á–µ—Å–∫–∏—Ö —Ç–µ—Ä–º–∏–Ω–∞—Ö, –ø–∏—à–∏—Ç–µ –∫–∞–∫ –µ—Å—Ç—å."
        )

    elif state == 'ask_description':
        user_states[user_id]['description'] = update.message.text
        user_states[user_id]['state'] = 'ask_confirmation'
        
        data = user_states[user_id]
        
        # --- –ù–û–í–´–ô –ë–õ–û–ö –ü–û–î–¢–í–ï–†–ñ–î–ï–ù–ò–Ø ---
        confirmation_text = (
            "**–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ –≤–∞—à—É –∑–∞—è–≤–∫—É:**\n\n"
            f"üë§ **–í–∞—à–µ –∏–º—è:** {data['name']}\n"
            f"üìÇ **–¢–µ–º–∞:** {data['category']}\n"
            "üìù **–û–ø–∏—Å–∞–Ω–∏–µ:**\n"
            f"_{data['description']}_\n\n"
            "–í—Å—ë –≤–µ—Ä–Ω–æ?"
        )
        keyboard = [
            [InlineKeyboardButton("‚úÖ –î–∞, –æ—Ç–ø—Ä–∞–≤–∏—Ç—å", callback_data='confirm_send')],
            [InlineKeyboardButton("‚úèÔ∏è –ù–∞—á–∞—Ç—å –∑–∞–Ω–æ–≤–æ", callback_data='show_services_menu')]
        ]
        
        await update.message.reply_text(confirmation_text, reply_markup=InlineKeyboardMarkup(keyboard), parse_mode='Markdown')


def main() -> None:
    application = Application.builder().token(BOT_TOKEN).build()

    application.add_handler(CommandHandler("start", start))
    application.add_handler(CallbackQueryHandler(button_handler))
    application.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, message_handler))
    application.add_handler(MessageHandler(filters.PHOTO | filters.Document.ALL, message_handler))

    application.run_polling()

if __name__ == "__main__":
    main()
